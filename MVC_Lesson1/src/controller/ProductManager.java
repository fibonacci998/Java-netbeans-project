    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        if (chkName.isSelected() && !chkPrice.isSelected()){
            if (txtName.getText().toString().compareTo("")==0 ){
                initTable("select * from SanPham");
            }else{
                initTable("select * from SanPham where sname like '%"+txtName.getText().toString()+"%'");
            }
        }
        if (!chkName.isSelected() && chkPrice.isSelected()){
            String from=txtFromPrice.getText().toString();
            String to=txtToPrice.getText().toString();
            if (from.compareTo("")==0){
                from="0";
            }
            if (to.compareTo("")==0){
                initTable("select * from SanPham where price >= "+from);
            }else{
                initTable("select * from SanPham where price >= "+from+" and price <= "+to);
            }
        }
        if (chkName.isSelected() && chkPrice.isSelected()){
            String from=txtFromPrice.getText().toString();
            String to=txtToPrice.getText().toString();
            String name=txtName.getText().toString();
            if (name.compareTo("")==0){
                if (to.compareTo("")==0){
                    initTable("select * from SanPham where price >= "+from);
                }else{
                    initTable("select * from SanPham where price >= "+from+" and price <= "+to);
                }
            }else{
                if (to.compareTo("")==0){
                    initTable("select * from SanPham where sname like '%"+name+"%' and price >= "+from);
                }else{
                    initTable("select * from SanPham where sname like '%"+name+"%' and price >= "+from+" and price <= "+to);
                }
            }

        }
    }//GEN-LAST:event_btnSearchActionPerformed

