    public void initTable(String sql){
        ResultSet rs=connDB.getData(sql);
        Boolean found = null;
        Vector row=new Vector();
        try {
            while (rs.next()){
                Vector temp=new Vector();
                temp.add(rs.getString(1));
                temp.add(rs.getString(2));
                temp.add(rs.getInt(3));
                temp.add(rs.getInt(4));
                temp.add(rs.getString(5));
                temp.add(rs.getString(6));
                temp.add(rs.getInt(7));
                temp.add(rs.getInt(8));
                row.add(temp);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ProductManager.class.getName()).log(Level.SEVERE, null, ex);
        }
        updateTable(row);
    }
    public void updateTableCart(Vector dataCart){
        tbCart.setModel(new DefaultTableModel(dataCart, colCart));
        int sum=0;
        for (Object element: dataCart){
            Vector temp=(Vector) element;
            sum+=(int)temp.get(4);
        }
        txtTotal.setText(sum+"");
        this.dataCart=dataCart;
    }
